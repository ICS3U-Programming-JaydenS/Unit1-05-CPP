// This program calculates the area and the perimeter of a rectangle.
#include <cmath>
#include <iostream>

int main() {
    std::cout << "If a rectangle has the dimensions" << std::endl;
    std::cout << "9cm x 13cm" << std::endl;
    std::cout << "" << std::endl;
    std::cout << "Area is: " << (9*13) << "cm^2" << std::endl;
    std::cout << "Perimeter is: " << ((9*2) +(13*2)) << "cm^2" << std::endl;
}
